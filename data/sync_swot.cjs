const fs = require('fs');
const path = require('path');

const swotDir = path.join(__dirname, 'swot');
const registryPath = path.join(__dirname, 'swotRegistry.ts');

const getAllFiles = (dirPath, arrayOfFiles) => {
  const files = fs.readdirSync(dirPath);
  arrayOfFiles = arrayOfFiles || [];

  files.forEach((file) => {
    if (fs.statSync(dirPath + "/" + file).isDirectory()) {
      arrayOfFiles = getAllFiles(dirPath + "/" + file, arrayOfFiles);
    } else if (file.endsWith('.json')) {
      arrayOfFiles.push(path.join(dirPath, "/", file));
    }
  });

  return arrayOfFiles;
};

const syncSwot = () => {
  const allJsonFiles = getAllFiles(swotDir);
  const swotData = {};

  allJsonFiles.forEach(filePath => {
    try {
      const fileContent = fs.readFileSync(filePath, 'utf8');
      if (!fileContent.trim()) return;

      const categoryData = JSON.parse(fileContent);
      
      // New structure: { category: "...", sectors: { "sector-id": { ... } } }
      if (categoryData.sectors) {
        Object.keys(categoryData.sectors).forEach(sectorId => {
          const data = categoryData.sectors[sectorId];
          if (data.description && data.description !== "") {
            swotData[sectorId] = data;
          }
        });
      } else {
        // Fallback for old structure (direct sector ID as file name)
        if (categoryData.description && categoryData.description !== "") {
          const sectorId = path.basename(filePath, '.json');
          swotData[sectorId] = categoryData;
        }
      }
    } catch (e) {
      console.error(`Error processing ${filePath}:`, e.message);
    }
  });

  const registryContent = `import { SwotAnalysis } from '../components/SectorDashboardTemplate/types';

/**
 * AUTO-GENERATED FILE - DO NOT EDIT MANUALLY
 * This file is generated by sync_swot.cjs based on JSON files in data/swot/
 */
export const SWOT_REGISTRY: Record<string, SwotAnalysis> = ${JSON.stringify(swotData, null, 2)};
`;

  fs.writeFileSync(registryPath, registryContent, 'utf8');
  console.log(`Successfully synced ${Object.keys(swotData).length} SWOT records to swotRegistry.ts`);
};

syncSwot();
